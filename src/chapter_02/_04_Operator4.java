package chapter_02;

public class _04_Operator4 {
    public static void main(String[] args) {
        boolean egg = true;
        boolean bread = true;
        boolean cake = false;

        System.out.println(egg || bread || cake);

        System.out.println((5 > 3) && (3 > 1));
        System.out.println((5 > 3) && (3 < 1));

        System.out.println((5 > 3) || (3 > 1));
        System.out.println((5 > 3) || (3 < 1));



    }
}
