package chapter_04;

public class _05_For {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++){
            System.out.println("What's up buddy");
        }
        for (int i = 0; i < 10; i++) {
            System.out.println("test");

        }
        int sum = 0;
        for (int i = 1; i <= 10; i++) {
            sum += i;
            System.out.println(sum);
        }
    }
}
